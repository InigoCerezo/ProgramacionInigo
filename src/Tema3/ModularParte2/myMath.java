package Tema3.ModularParte2;

public class myMath {
/// ----------------------EJERCICIO 1
    public static double trianglePerimeter(double a, double b, double c){
        return a+b+c;
    }
    public static double triangleArea(double a, double b){
        return (a*b)/2;
    }
    public static double squarePerimeter(double a){
        return a*4;
    }
    public static double squareArea(double a){
        return (a*2);
    }
    public static double circlePerimeter(double a){
        return 2 * Math.PI * a;
    }
    public static double circleArea(double a){
        return Math.PI * (a*a);
    }
/// ----------------------EJERCICIO 2

}
